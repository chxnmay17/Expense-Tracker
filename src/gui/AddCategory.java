    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           
        try {
            String category=t.getText();      
            if(!category.equals("")){
            db.dbconnection.s.executeUpdate("insert into category_info values('"+category+"')");
            JOptionPane.showMessageDialog(null,"Category Added Succesfully");
            getEntries();  
            }else{
             JOptionPane.showMessageDialog(null, "Empty category cannot be added");
            }
        } catch(SQLIntegrityConstraintViolationException ex){
        JOptionPane.showMessageDialog(null, "Category Already Exist");
        } catch (SQLException ex) {
            Logger.getLogger(AddCategory.class.getName()).log(Level.SEVERE, null, ex);
        }             
    }//GEN-LAST:event_jButton1ActionPerformed

