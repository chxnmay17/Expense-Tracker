    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ri=table.getSelectedRow();
        if(ri!=-1){
        int r=JOptionPane.showConfirmDialog(null, "Do you want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION, HEIGHT);
        if (r==JOptionPane.YES_OPTION){
        
        String category;
        category = (String) table.getValueAt(ri, 1);
           
           
        try {        
            db.dbconnection.s.executeUpdate("delete from category_info where category='"+category+"'");
            JOptionPane.showMessageDialog(null,"Category Deleted");
             getEntries();
        } catch (SQLException ex) {
            Logger.getLogger(AddCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

