    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            java.util.Date dt=d.getDate();
            String s1=a.getText();
            String s2=(String) category.getSelectedItem();
            if(dt!=null && !s1.equals("")&& !s2.equals("")){
                int amount=Integer.parseInt(s1);
               java.sql.Date date=new java.sql.Date(dt.getTime());
                try {
                    db.dbconnection.s.executeUpdate("insert into spendings (category,sdate,amount) values(' "+s2+" ',' "+ date +" ', "+amount+")");
                } catch (SQLException ex) {
                    Logger.getLogger(ExpenseTracker.class.getName()).log(Level.SEVERE, null, ex);
                }
                  JOptionPane.showMessageDialog(null,"Expense Added !");
                  getEntries();
            }
            else{
                JOptionPane.showMessageDialog(null,"Please Enter All Details");
            }
    }//GEN-LAST:event_jButton3ActionPerformed

