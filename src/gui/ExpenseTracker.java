    private void displayCategory(){
        
        try {
            category.removeAllItems();
            ResultSet rs= db.dbconnection.s.executeQuery("select * from category_info");
            while(rs.next()){
                category.addItem(rs.getString("category"));
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(ExpenseTracker.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
private    void getEntries(){
    try{ 
       javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel) table.getModel();
       int rc=dtm.getRowCount();
       while(rc--!=0){
       dtm.removeRow(0);
           
       }
       java.time.LocalDate cd=java.time.LocalDate.now();
        java.time.LocalDate bd=cd.minusDays(32);
        ResultSet rs=db.dbconnection.s.executeQuery("select * from spendings where sdate<='"+cd +"' and sdate>='"+bd+" 'order by sdate asc " ) ;
       int total=0;
        while(rs.next()){
            total+=rs.getInt("amount");
            Object o[]={rs.getInt("sid"),rs.getString("category"),rs.getDate("sdate"),rs.getInt("amount")};
                       dtm.addRow(o);
        }
        totalAmount.setText(total+"");
    }catch(Exception ex){
    JOptionPane.showMessageDialog(null,ex);
    }
}
    
    
    
