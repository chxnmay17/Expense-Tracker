    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int ri=table.getSelectedRow();
       if(ri!=-1){
           int r=JOptionPane.showConfirmDialog(null, "Do you want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION, HEIGHT);
        if (r==JOptionPane.YES_OPTION){
           int id=(int) table.getValueAt(ri, 0);
           try {
               db.dbconnection.s.executeUpdate("delete from spendings where sid="+id+"");
               JOptionPane.showMessageDialog(null,"Selected Entry Deleted");
               getEntries();      
           } catch (SQLException ex) {
               Logger.getLogger(ExpenseTracker.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

